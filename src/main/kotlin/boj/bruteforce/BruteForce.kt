 * 2. 정답이 될 수 있는 경우 - 정답이 되는지 체크하고 리턴
 * 3. 더이상 재귀가 불가능한 경우 - 리턴
 * 4. 더 재귀해보아야하는 경우 - 현 상황에서 다음으로 재귀
 * (이 때 `준비 - 재귀 - 복원 - 다음시도` 프로세스를 신경쓴다. 복원해야 다음 시도할 때 영향을 안받을 수 있으니까.)
 *
 * 모든 N 개에서 1~N 몇개를 뽑든 모든 조합을 다 해보는 가장 쉬운 방법은 각 원소를 포함해보고 안해보고 두 가지로 재귀하는 방식이다.
 *
 */